        options ??= new JsonFormatOptions();
        
        var jsonOptions = new JsonSerializerOptions
        {
            WriteIndented = options.Indent,
            DefaultIgnoreCondition = options.IncludeNulls 
                ? System.Text.Json.Serialization.JsonIgnoreCondition.Never
